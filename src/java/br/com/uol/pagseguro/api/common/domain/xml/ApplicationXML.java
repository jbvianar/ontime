package br.com.uol.pagseguro.api.common.domain.xml;

import br.com.uol.pagseguro.api.common.domain.Application;

/**
 * Implementation of {@code Application}
 *
 * @author PagSeguro Internet Ltda.
 */
public class ApplicationXML implements Application {
  private String id;
  private String name;
  private String role;

  public String getId() {
    return id;
  }

  public void setId(String id) {
    this.id = id;
  }

  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }

  public String getRole() {
    return role;
  }

  public void setRole(String role) {
    this.role = role;
  }

  @Override
  public String toString() {
    return "ApplicationXML{" +
        "id='" + id + '\'' +
        ", name='" + name + '\'' +
        ", role='" + role + '\'' +
        '}';
  }
}
